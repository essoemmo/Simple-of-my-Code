<script>

        $('body').on('submit','#addcategory',function (e) {
            e.preventDefault();
            $.ajax({
                url: '{{ route('categories.store') }}',
                method: "post",
                data: new FormData(this),
                dataType: 'json',
                cache       : false,
                contentType : false,
                processData : false,
                success: function (response) {
                if(response.status == 'success'){
                    toastr.options =
                    {
                        "closeButton" : true,
                        "progressBar" : true,
                        "showDuration": 500, 
                    }
                    toastr['success']("@lang('admin.added')");
                }
                    $('.category-table').DataTable().ajax.reload();
                    $modal = $('#modal-add-category');
                    $modal.find('form')[0].reset();
                    $('#modal-add-category').modal('hide');
                },
                error: function (response) {
                    var errors = response.responseJSON.errors;
                    $.each(errors, function( index, value ) {
                    toastr.options ={
                            "closeButton" : true,
                            "progressBar" : true,
                            "showDuration": 500, 
                        }
                    toastr['error'](value);
                    });
                }


            });
      });

      $('body').on('click','#check',function () {
            //e.preventDefault();
            var active = $(this).prop('checked') == true ? 1 : 0; 
            var category_id = $(this).data('id');
            $.ajax({
                url:'{{ route('categoryactive') }}',
                type:'GET',
                data:{
                    'active': active,
                    'category_id': category_id
                },
                success: function (response) {
                if (response.status == 'success'){
                        toastr.options =
                        {
                            "closeButton" : true,
                            "progressBar" : true,
                            "showDuration": 500, 
                            // "rtl": isRtl 
                        }
                        toastr['success']("@lang('admin.statuschange')");
                    }
                }
            });
        });

      $('body').on('click','.edit',function (e) {

            e.preventDefault();
            var id = $(this).data('categoryid');
            var title_ar = $(this).data('title_ar');
            var title_en = $(this).data('title_en');

            $('#Category_id').val(id);
            $('#title_ar').val(title_ar);
            $('#title_en').val(title_en);


        })

      $('body').on('submit','#editcategory',function (e) {
                e.preventDefault();
                let id = $('#Category_id').val();
                var url = '{{ route('categories.update', ':id') }}';
                url = url.replace(':id', id);

                $.ajax({
                    url: url,
                    method: "post",
                    data: new FormData(this),
                    dataType: 'json',
                    cache       : false,
                    contentType : false,
                    processData : false,
                    success: function (response) {
                    if(response.status == 'success'){
                        toastr.options = {
                            "closeButton" : true,
                            "progressBar" : true,
                            "showDuration": 500, 
                        }
                        toastr['success']("@lang('admin.updated')");
                    }
                    $('.category-table').DataTable().ajax.reload();
                    $modal = $('#modal-edit-category');
                    $modal.find('form')[0].reset();
                    $('#modal-edit-category').modal('hide');
                    },
                    error: function (response) {
                     var errors = response.responseJSON.errors;
                     $.each(errors, function( index, value ) {
                    toastr.options =
                    {
                        "closeButton" : true,
                        "progressBar" : true,
                        "showDuration": 500, 
                    }
                    toastr['error'](value);
                });
                }

                });
       });

      $('body').on('submit','#delform',function (e) {
           e.preventDefault();
           var url = $(this).attr('action');
           $.ajax({
             url: url,
             method: "delete",
             data: {
                 _token: '{{ csrf_token() }}',
             },
             success: function (response) {
                $('.category-table').DataTable().ajax.reload();
             }
           });
        })
    
</script>
